    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, I18n.get("register_validation_empty"), I18n.get("error"), JOptionPane.ERROR_MESSAGE);
            return;
        }

        Document doc = MongoUtil.getDB().getCollection("users")
                .find(new Document("username", username)).first();

        if (doc == null) {
            JOptionPane.showMessageDialog(this, I18n.get("user_not_found"), I18n.get("error"), JOptionPane.ERROR_MESSAGE);
            return;
        }

        String hash = doc.getString("passwordHash");
        if (CryptoUtil.checkPassword(password, hash)) {
            JOptionPane.showMessageDialog(this, I18n.get("login_success"), I18n.get("success"), JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new MainFrame(username).setVisible(true);  // ‚Üê Buka MainFrame dengan username
        } else {
            JOptionPane.showMessageDialog(this, I18n.get("wrong_password"), I18n.get("error"), JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnLoginActionPerformed

